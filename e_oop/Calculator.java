package e_oop;

public class Calculator {
//산술 연산
	
	
	double add(double a, double b){
		return a+b;
	}
	

	double sub(double a, double b){
		return a-b;
	}
	

	double mul(double a, double b){
		return a*b;
	}
	

	double div(double a, double b){
		return a/b;
	}
	
	double rem(double a, double b){
		return a%b;
	}
	
	
}
